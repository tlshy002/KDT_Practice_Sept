package sept05;

public class Lesson02 {

	public static void main(String[] args) {
		int i = 111;
		int j = 222;
		
		//문자열의 데이터타입은 String
		String k = "";
		String l = "10";
		
		//콘솔에 데이터 출력
		System.out.println(i+j); //숫자 + 숫자
		System.out.println(i+k); //숫자 + 공백문자열
		System.out.println(i+l); //숫자 + 문자열
		System.out.println(l+l); //문자열 + 문자열
		

	}

}
